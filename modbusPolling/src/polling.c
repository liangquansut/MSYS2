int start_thread(thread_t *t, thread_ret_t (THREAD_CALL *func)(void*), void *arg) {
    *t = CreateThread(NULL, 0, func, arg, 0, NULL);
    return *t ? 0 : -1;
}

/**
 * 等待线程结束并清理资源
 * 参数t：线程句柄
 * 返回值：无返回值
 */
void join_thread(thread_t t) {
    WaitForSingleObject(t, INFINITE);
    CloseHandle(t);
}
